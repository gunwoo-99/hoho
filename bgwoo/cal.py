# 계산기를 만들거임
class Calculator():
    def __init__(self):
        pass

# 더하기
    def plus(self, num1, num2):
        result = num1 + num2
        return result
    
# 뺴기
    def minus(self, num1, num2):
        result = num1 - num2
        return result

# 곱하기
    def multiply(self, num1, num2):
        result = num1 * num2
        return result

# 나누기
    def divide(self, num1, num2):
        result = num1 / num2
        return result